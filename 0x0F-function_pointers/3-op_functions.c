#include <stdio.h>
/**
 * op_add - summation operation.
 * @a: array of elements.
 * @b: the size of the array.
 * Return: sum of type integer.
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - Difference opertaion.
 * @a: array of elements.
 * @b: the size of the array.
 * Return: the difference of type integer.
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplication operation.
 * @a: array of elements.
 * @b: the size of the array.
 * Return: the difference of type integer.
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - division operation.
 * @a: array of elements.
 * @b: the size of the array.
 * Return: the difference of type integer.
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - remainder of the devision operation.
 * @a: array of elements.
 * @b: the size of the array.
 * Return: the difference of type integer.
 */
int op_mod(int a, int b)
{
return (a % b);
}
